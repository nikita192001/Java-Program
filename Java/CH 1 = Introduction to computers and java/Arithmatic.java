// Wap to perform Arthmatic operation on 2 numbers.( Addiition, Substraction , Multiplication, Division)

class Arithmatic
	{
	 
	public static void main(String args[])
	    {
		int a=10, b=20, add, sub, mul, div;
		System.out.print("\n\n A="+a + "\n\n B="+b);
		
		add=a+b;
		sub=a-b;
		mul=a*b;
		div=a/b;

		System.out.print("\n\n Addition=" +add);

		System.out.print("\n\n Substraction=" +sub);

		System.out.print("\n\n Multiplication=" +mul);

		System.out.print("\n\n Division=" +div);
	     }
	}